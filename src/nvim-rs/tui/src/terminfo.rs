/// Look up a builtin terminfo entry for the given $TERM string.
///
/// # Safety
/// `term` must be a valid NUL-terminated C string or null.
/// `termname` must be a valid pointer to write to.
#[no_mangle]
pub unsafe extern "C" fn rs_terminfo_from_builtin(
    term: *const c_char,
    termname: *mut *mut c_char,
) -> *const TerminfoEntry {
    use crate::is_term_family;

    let term_bytes = if term.is_null() {
        b"" as &[u8]
    } else {
        std::ffi::CStr::from_ptr(term).to_bytes()
    };

    let (name, entry): (&[u8], &'static TerminfoEntry) = if is_term_family(term_bytes, b"xterm") {
        (b"xterm\0", &XTERM_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"screen") {
        (b"screen\0", &SCREEN_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"tmux") {
        (b"tmux\0", &TMUX_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"rxvt") {
        (b"rxvt\0", &RXVT_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"putty") {
        (b"putty\0", &PUTTY_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"linux") {
        (b"linux\0", &LINUX_16COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"interix") {
        (b"interix\0", &INTERIX_8COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"iterm")
        || is_term_family(term_bytes, b"iterm2")
        || is_term_family(term_bytes, b"iTerm.app")
        || is_term_family(term_bytes, b"iTerm2.app")
    {
        (b"iterm\0", &ITERM_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"st") {
        (b"st\0", &ST_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"gnome") || is_term_family(term_bytes, b"vte") {
        (b"vte\0", &VTE_256COLOUR_TERMINFO)
    } else if is_term_family(term_bytes, b"cygwin") {
        (b"cygwin\0", &CYGWIN_TERMINFO)
    } else if is_term_family(term_bytes, b"win32con") {
        (b"win32con\0", &WIN32CON_TERMINFO)
    } else if is_term_family(term_bytes, b"conemu") {
        (b"conemu\0", &CONEMU_TERMINFO)
    } else if is_term_family(term_bytes, b"vtpcon") {
        (b"vtpcon\0", &VTPCON_TERMINFO)
    } else {
        (b"ansi\0", &ANSI_TERMINFO)
    };

    if !termname.is_null() {
        *termname = name.as_ptr() as *mut c_char;
    }
    entry as *const TerminfoEntry
}
