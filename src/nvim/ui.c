/// Wrapper: call ui_call_chdir with current working directory
void nvim_ui_call_chdir_cwd(void)
{
  char cwd[MAXPATHL];
  size_t cwdlen = sizeof(cwd);
  if (uv_cwd(cwd, &cwdlen) == 0) {
    ui_call_chdir((String){ .data = cwd, .size = cwdlen });
  }
}

